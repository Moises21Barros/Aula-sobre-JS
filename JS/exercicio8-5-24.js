let diaSemana = "a"

// A - Domingo
// B - Segunda-feira
// C - Terça-feira
// D - Quarta-feira
// E - Quinta-feira
// F - Sexta-feira
// G - Sábado

switch (diaSemana) {
    case "A":
        console.log("Domingo");
    break;
    case "B":
        console.log("Segunda-feira");
    break;
    case "C":
        console.log("Terça-feira");
    break;
    case "D":
        console.log("Quarta-feira");
    break;
    case "E":
        console.log("Quinta-feira");
    break;
    case "F":
        console.log("Sexta-feira");
    break;
    case "G":
        console.log("Sábado");
    break;
    default: console.log("Dia Inválido");
}